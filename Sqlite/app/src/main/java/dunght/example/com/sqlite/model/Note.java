package dunght.example.com.sqlite.model;

public class Note {

    private int id;
    private String note;
    private String date;

    public Note() {
    }

    public Note(String note, String date) {
        this.note = note;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
